        private void deleteStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_deleteStudentButtonActionPerformed
                // TODO add your handling code here:
                String statusLog = "";
                String studentID = StudentQueries.getStudentID(((String) deleteStudentComboBox.getSelectedItem()));

                StudentQueries.dropStudent(studentID);
                statusLog = String.format("%s %s has been dropped from the list of students\n",
                                (String) deleteStudentComboBox.getSelectedItem(), studentID);

                for (String semester : SemesterQueries.getSemesterList()) {
                        statusLog += String.format("\nFor Semester: %s\n", semester);
                        ArrayList<ScheduleEntry> deletedStudentSchedule = ScheduleQueries.getScheduleByStudent(semester,
                                        studentID);
                        for (ScheduleEntry scheduleEntry : deletedStudentSchedule) {
                                String courseCode = scheduleEntry.getCourseCode();

                                if (ScheduleQueries.isStudentWaitlisted(semester, studentID, courseCode)) {
                                        statusLog += String.format("%s %s has been dropped from the waitlist for %s\n",
                                                        (String) deleteStudentComboBox.getSelectedItem(), studentID,
                                                        courseCode);
                                } else {
                                        statusLog += String.format("%s %s has been dropped from %s\n",
                                                        (String) deleteStudentComboBox.getSelectedItem(), studentID,
                                                        courseCode);
                                }
                                ;
                                ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, courseCode);

                                int scheduledStudentCount = ScheduleQueries.getScheduledStudentCount(semester,
                                                courseCode);

                                int seats = ClassQueries.getClassSeats(semester, courseCode);
                                if (seats > scheduledStudentCount) {
                                        ArrayList<ScheduleEntry> allWaitlistClass = ScheduleQueries
                                                        .getWaitlistedStudentsByClass(semester,
                                                                        courseCode);
                                        if (!allWaitlistClass.isEmpty()) {
                                                ScheduleQueries.updateScheduleEntry(allWaitlistClass.get(0));
                                                StudentEntry student = StudentQueries
                                                                .getStudent(allWaitlistClass.get(0).getStudentID());
                                                statusLog += String.format("%s, %s %s has been scheduled into %s\n",
                                                                student.getLastName(),
                                                                student.getFirstName(), student.getStudentID(),
                                                                allWaitlistClass.get(0).getCourseCode());
                                        }
                                }
                        }

                }
                deleteStudentStatusText.setText(statusLog);
                updateSelectStudentComboBox();
        }// GEN-LAST:event_deleteStudentButtonActionPerformed

