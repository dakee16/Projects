        private void dropStudentClassButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_dropStudentClassButtonActionPerformed
                // TODO add your handling code here:
                String statusLog = "";
                String semester = (String) currentSemesterComboBox.getSelectedItem();
                String courseCode = (String) dropStudentClassComboBox.getSelectedItem();
                String studentID = StudentQueries
                                .getStudentID(((String) dropStudentClassStudentComboBox.getSelectedItem()));

                ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, courseCode);
                StudentEntry student = StudentQueries.getStudent(studentID);
                statusLog += String.format("%s, %s %s has been dropped from %s\n",
                                student.getLastName(),
                                student.getFirstName(), student.getStudentID(),
                                courseCode);

                int scheduledStudentCount = ScheduleQueries.getScheduledStudentCount(semester, courseCode);
                int seats = ClassQueries.getClassSeats(semester, courseCode);
                if (seats > scheduledStudentCount) {
                        ArrayList<ScheduleEntry> allWaitlistClass = ScheduleQueries
                                        .getWaitlistedStudentsByClass(semester,
                                                        courseCode);
                        if (!allWaitlistClass.isEmpty()) {
                                ScheduleQueries.updateScheduleEntry(allWaitlistClass.get(0));
                                StudentEntry studentWait = StudentQueries
                                                .getStudent(allWaitlistClass.get(0).getStudentID());
                                statusLog += String.format("%s, %s %s has been scheduled into %s\n",
                                                studentWait.getLastName(),
                                                studentWait.getFirstName(), studentWait.getStudentID(),
                                                allWaitlistClass.get(0).getCourseCode());
                        }
                }

                dropStudentClassStatusTextBox.setText(statusLog);

        }// GEN-LAST:event_dropStudentClassButtonActionPerformed

